package curriculumB;

import java.util.Scanner;

public class Qes2 {

	public static void main(String[] args) {
		
		/*----------------------------------
		 [概要] 合否判定
		 [詳細] scoreの値が60より大きければ「合格」、60未満なら「不合格」と表示する。
		----------------------------------*/
		// 型に変数を設定し、値を代入。
		int score = 75;
		// もし変数の値が60より大きい場合、ブロック内を適用
		if ( score >= 60 ) {
			// コンソール画面に"合格です！"と表示する。
			System.out.println( "合格です！" );
		// 上記以外の場合
		}else{
			// コンソール画面に"不合格です。"と表示する。
			System.out.println( "不合格です。" );
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 年齢の範囲チェック
		 [詳細] ageが20以上かつ30以下であれば「適正年齢」、それ以外は「不適正」と表示する。
		----------------------------------*/
		// 型に変数を設定し、値を代入。
		int age = 25;
		// もし変数の値が20以上、30以下の場合、ブロック内を適用
		if( 20 <= age && 30 >= age ){
			// コンソール画面に"適正年齢です"と表示する。
			System.out.println( "適正年齢です" );
		// 上記以外の場合
		}else{
			// コンソール画面に"適正年齢ではありません"と表示する。
			System.out.println( "適正年齢ではありません" );	
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 年齢による区分表示
		 [詳細] ageの値に応じて「成人」「ティーンエイジャー」「子ども」と分類して出力する。
		----------------------------------*/
		// 値を再代入。
		age = 18;
		// もし変数の値が20以上の場合、ブロック内を適用
		if( 20 <= age ){
			// コンソール画面に"成人です"と表示する。
			System.out.println( "成人です" );
		// もし変数の値が13以上、19以下の場合、ブロック内を適用
		}else if( 13 <= age && 19 >= age ){
			// コンソール画面に"ティーンエイジャーです"と表示する。
			System.out.println( "ティーンエイジャーです" );
		// もし変数の値が12以下の場合、ブロック内を適用
		}else if( 12 >= age ){
			// コンソール画面に"子どもです"と表示する。
			System.out.println( "子どもです" );
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 最大値の判定
		 [詳細] x, y, zの中で最も大きい値を判定し、それを出力する。
		----------------------------------*/
		// 型に変数を設定し、値を代入。
		int x = 30;
		int y = 15;
		int z = 50;
		// xがy、z以上の場合、ブロック内を適用
		if(x >= y && x >= z){
			// コンソール画面に"一番大きいのは30です。"と表示する。
			System.out.println( "一番大きいのは" + x + "です。" );
		// yが最大の場合、ブロック内を適用
		}else if(y >= x && y >= z){
			// コンソール画面に"一番大きいのは15です。"と表示する。
			System.out.println( "一番大きいのは" + y + "です。" );
		// 上記以外の場合
		}else{
			// コンソール画面に"一番大きいのは50です。"と表示する。
			System.out.println( "一番大きいのは" + z + "です。" );
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 数値の正負判定
		 [詳細] numの値が正、負、または0であるかを判定して表示する。
		----------------------------------*/
		// 型に変数を設定し、値を代入。
		int num = 0;
		// もし変数の値が0より大きい場合、ブロック内を適用
		if( num > 0){
			// コンソール画面に"正の数です"と表示する。
			System.out.println( "正の数です" );
		// もし変数の値と0が同じ場合、ブロック内を適用
		}else if( num == 0){
			// コンソール画面に"0です。"と表示する。
			System.out.println( "0です" );
		// もし変数の値が0より小さい場合、ブロック内を適用
		}else if( num < 0){
			// コンソール画面に"負の数です"と表示する。
			System.out.println( "負の数です" );
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 偶数・奇数の判定
		 [詳細] numが偶数か奇数かを判定して表示する。
		----------------------------------*/
		// 値を再代入。
		num = 1;
		// もし変数の値を2で割った余りが0の場合、ブロック内を適用
		if( num % 2 == 0 ){
			// コンソール画面に"偶数です"と表示する。
			System.out.println( "偶数です" );
		// 上記以外の場合
		}else{
			// コンソール画面に"奇数です"と表示する。
			System.out.println( "奇数です" );
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 成績の評価
		 [詳細] scoreの値に応じて「優」「良」「可」「不可」を表示する。
		----------------------------------*/
		// 値を再代入。
		score = 50;
		// もし変数の値が90点以上の場合、ブロック内を適用
		if( 90 <= score ) {
			// コンソール画面に"優"と表示する。
			System.out.println( "優" );
		// もし変数の値が70点以上の場合、ブロック内を適用
		}else if( 70 <= score ) {
			// コンソール画面に"良"と表示する。
			System.out.println( "良" );
		// もし変数の値が50点以上の場合、ブロック内を適用
		}else if( 50 <= score ) {
			// コンソール画面に"可"と表示する。
			System.out.println( "可" );
		// 上記以外の場合
		}else{
			// コンソール画面に"不可"と表示する。
			System.out.println( "不可" );
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 文字列の空チェック
		 [詳細] textが空文字かどうかを判定し、無効か有効かを表示する。
		----------------------------------*/
		// Scannerの作成
		Scanner scanner = new Scanner(System.in);
		// コンソール画面に入力を促すメッセージを表示
		System.out.print("文字を入力してください：");
		// 型に変数を設定し、ユーザーが入力した文字を変数に代入。
		String text = scanner.nextLine();
		// もし変数の値が空文字だった場合、ブロック内を適用
		if( text.isEmpty()) {
			// コンソール画面に"入力が無効です"と表示する。
			System.out.println( "入力が無効です" );
		// 上記以外の場合
		}else {
			// コンソール画面に変数textを表示する。
			System.out.println( text );
		}
		// Scannerを閉じる
		scanner.close();
		System.out.println();
		
		/*----------------------------------
		 [概要] 曜日の表示
		 [詳細] dayの値に応じて曜日名を表示し、それ以外は無効な入力とする。
		----------------------------------*/
		// 型に変数を設定し、値を代入。
		int day = 1;
		// dayの値に応じて処理を適用
		switch( day ) {
			// dayの値が1の場合
			case 1:
				// コンソール画面に"月曜日"と表示する。
				System.out.println( "月曜日" );
				// 処理後、switch文を抜ける
				break;
			// dayの値が2の場合
			case 2:
				// コンソール画面に"火曜日"と表示する。
				System.out.println( "火曜日" );
				// 処理後、switch文を抜ける
				break;
			// dayの値が3の場合
			case 3:
				// コンソール画面に"水曜日"と表示する。
				System.out.println( "水曜日" );
				// 処理後、switch文を抜ける
				break;
			// dayの値が4の場合
			case 4:
				// コンソール画面に"木曜日"と表示する。
				System.out.println( "木曜日" );
				// 処理後、switch文を抜ける
				break;
			// dayの値が5の場合
			case 5:
				// コンソール画面に"金曜日"と表示する。
				System.out.println( "金曜日" );
				// 処理後、switch文を抜ける
				break;
			// dayの値が6の場合
			case 6:
				// コンソール画面に"土曜日"と表示する。
				System.out.println( "土曜日" );
				// 処理後、switch文を抜ける
				break;
			// dayの値が7の場合
			case 7:
				// コンソール画面に日曜日"と表示する。
				System.out.println( "日曜日" );
				// 処理後、switch文を抜ける
				break;
			// 上記以外の場合
			default:
				// コンソール画面に"無効な入力です"と表示する。
				System.out.println( "無効な入力です" );
				// 処理後、switch文を抜ける
				break;
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 月から季節を判定
		 [詳細] monthの値に応じて季節（春・夏・秋・冬）を判定して表示する。
		----------------------------------*/
		// 型に変数を設定し、値を代入。
		int month = 1;
		// monthの値に応じてcaseを適用
		switch( month ) {
			// monthの値が12,1,2の場合
			case 12, 1, 2:
				// コンソール画面に"冬"と表示する。
				System.out.println( "冬" );
				// 処理後、switch文を抜ける
				break;
			// monthの値が3,4,5の場合
			case 3, 4, 5:
				// コンソール画面に"春"と表示する。
				System.out.println( "春" );
				// 処理後、switch文を抜ける
				break;
			// monthの値が6,7,8の場合
			case 6, 7, 8:
				// コンソール画面に"夏"と表示する。
				System.out.println( "夏" );
				// 処理後、switch文を抜ける
				break;
			// monthの値が9,10,11の場合
			case 9, 10, 11:
				// コンソール画面に"秋"と表示する。
				System.out.println( "秋" );
				// 処理後、switch文を抜ける
				break;
			// 上記以外の場合
			default:
				// コンソール画面に"無効な月です"と表示する。
				System.out.println( "無効な月です" );
				// 処理後、switch文を抜ける
				break;
		}
	}
}
