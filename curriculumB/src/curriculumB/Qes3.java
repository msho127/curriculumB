package curriculumB;

import java.util.Random;
import java.util.Scanner;


public class Qes3 {

	public static void main(String[] args) {
	
		/*----------------------------------
		 [概要] 1から10までの表示
		 [詳細] for文を使って1から10までの整数を順に出力する。
		----------------------------------*/
		// 初期値1を1ずつ増やし10以上になった時、動作を終了する。
		for (int i = 1; i <= 10; i++){
			// コンソール画面にiの値を表示
			System.out.println(i);
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 偶数の出力
		 [詳細] 2から20までの偶数をfor文で出力する。
		----------------------------------*/
		// 初期値2を2ずつ増やし20以上になった時、動作を終了する。
		for (int i = 2; i <= 20; i += 2){
			// コンソール画面にiの値を表示
			System.out.println(i);
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] カウントダウンの表示
		 [詳細] for文を使って10から1までの数を逆順で出力する。
		----------------------------------*/
		// 初期値10を1ずつ減らし1以下になった時、動作を終了する。
		for (int i = 10; i >= 1; i--){
			// コンソール画面にiの値を表示
			System.out.println(i);
		}
		System.out.println();
		
		/*----------------------------------
		 [概要] 合計値の計算
		 [詳細] 1から100までの整数を加算し、その合計を出力する。
		----------------------------------*/
		// 型に変数を設定し、値を代入。
        int sum = 0;
        // 初期値1を1ずつ増やし100以上になった時、動作を終了する。
        for (int i = 1; i <= 100; i++) {
        	// iの値を変数sumに足す
            sum += i;
        }
        // コンソール画面に合計の値を表示
        System.out.println("1から100までの合計は: " + sum);
        System.out.println();
        
        /*----------------------------------
        [概要] アスタリスクによる三角形の表示
        [詳細] for文を使って1行ごとにアスタリスクの数を1つずつ増やして5行の三角形を出力する。
       ----------------------------------*/
       for (int i = 1; i <= 5; i++) {
           // アスタリスクを連結して出力するための変数
           String line = "";
           // iの回数分アスタリスクを追加
           for (int j = 1; j <= i; j++) {
               line += "*";
           }
           // 完成した1行を出力
           System.out.println(line);
		}
		System.out.println();
        
        
		/*----------------------------------
        [概要] while文で1から10までの表示
 		[詳細] 変数countの値を1から順に1ずつ増やしながら10まで出力する。
       ----------------------------------*/
		// 型に変数を設定し、値を代入。
		int count = 1;
		// 変数countが10以上になった時、動作を終了する。
		while (count <= 10) {
			// コンソール画面に変数countを表示
			System.out.println(count);
			// 変数countを1増やす
			count++;
		};
		System.out.println();
        
		/*----------------------------------
        [概要] while文で偶数の出力
 		[詳細] countを2から20まで2ずつ増やして偶数を出力する。
       ----------------------------------*/
		// 型に変数を設定し、値を代入。
		count = 2;
		// 変数countが20以上になった時、動作を終了する。
		while (count <= 20) {
			// コンソール画面に変数countを表示
			System.out.println(count);
			// 変数countを2増やす
			count += 2;
		};
		System.out.println();
		
		/*----------------------------------
         [概要] while文でカウントダウン
 		 [詳細] countを10から1まで1ずつ減らして出力する。
       ----------------------------------*/
		// 型に変数を設定し、値を代入。
		count = 10;
		// 変数countが1以下になった時、動作を終了する。
		while (count >= 1) {
			// コンソール画面に変数countを表示
			System.out.println(count);
			// 変数countを1減らす
			count--;
		};
		System.out.println();
        
		/*----------------------------------
        [概要] while文で1から100までの合計値を算出
 		[詳細] countを1から100まで加算し、合計sumを出力する。
       ----------------------------------*/
		// 型に変数を設定し、値を代入。
		count = 1;
		sum = 0;
		// 変数countが100以上になった時、動作を終了する。
		while (count <= 100) {
			// 変数sumに変数countの値を足す
			sum += count;
			// 変数countを1増やす
			count++;
		};
		// コンソール画面に変数sumの値を表示
		System.out.println( sum );
		System.out.println();
        
		/*----------------------------------
       	[概要] 0が入力されるまで繰り返し数値を入力させる処理
 		[詳細] Scannerを使用して数値を入力し、0が入力されるまで繰り返す。
       ----------------------------------*/
		// Scannerの作成
		Scanner scanner = new Scanner(System.in);
		// 型に変数を設定し、値を代入。(0以外)
		int number = -1;
        // 変数numberの値が0以外の時、繰り返し処理を行う。
        while (number != 0) {
        	// コンソール画面に"数値を入力してください（0で終了）："と表示する。
            System.out.print("数値を入力してください（0で終了）：");
            // ユーザーが入力した数値を変数numberに代入する。
            number = scanner.nextInt();
        }
        // while文が終了した時、コンソール画面に"終了しました。"と表示する。
        System.out.println("終了しました。");
        // Scannerを閉じる
        // scanner.close();
        System.out.println();
        
        /*----------------------------------
       	[概要] 九九表の出力
 		[詳細] for文の入れ子構造を使い、1×1 から 9×9 までの掛け算結果を出力する。
       ----------------------------------*/
        // 初期値1を1ずつ増やし9以上になった時、動作を終了する。
        for (int i = 1; i <= 9; i++){
        	// 初期値1を1ずつ増やし9以上になった時、動作を終了する。
        	for (int j = 1; j <= 9; j++){
        		// コンソール画面に縦の段、横の列、それぞれをかけた数字を表示する。
    			System.out.print(i + "*" + j + "=" + ( i * j ) + " || ");
        	}
        	// 段を揃えるための改行
        	System.out.println();
		} 
        System.out.println();
        
        
        /*----------------------------------
        [概要] 複数の家電製品をカンマ区切りで入力し、それぞれの製品の在庫台数をランダムに表示するプログラム。
        [詳細] ユーザーからカンマ区切りで複数の家電名を入力として受け取り、各商品に対してランダムな残り台数を出力する。
       ----------------------------------*/
        // コンソール画面に"ここに家電を入力："と表示し入力を促す
        System.out.print("ここに家電を入力：");
        // 入力された家電名を「、」で区切り、配列に分割して格納
        String[] products = scanner.next().split("、");

        // ランダム値を生成するためのRandomオブジェクトを作成
        Random rand = new Random();

        // テレビの在庫数（0〜11）をランダムに決定
        int TVnum = rand.nextInt(12);
        // ディスプレイの在庫数をテレビと合わせて11台になるように調整
        int DPnum = 11 - TVnum;

        // 入力された家電製品ごとに処理を行う
        for (String product : products) {
            // 各家電の在庫台数を0〜11の範囲でランダムに決定
            int num = rand.nextInt(12);

            // 商品名に応じて処理を分岐
            switch (product) {
                // 以下の製品の場合は共通処理でランダムな在庫台数を表示
                case "パソコン", "冷蔵庫", "扇風機", "洗濯機", "加湿器":
                	// コンソール画面に 商品目 "の残りの台数は" 数 "台です" と表示する。
                    System.out.println(product + "の残り台数は" + num + "台です");
                    // 処理後、Switch文を抜ける。
                    break;

                // 「テレビ」と「ディスプレイ」は特別処理で在庫数を固定割り当て
                case "テレビ", "ディスプレイ":
                    // 三項演算子でテレビかディスプレイかを判別し、それぞれの在庫数を表示
                    String product1 = product.equals("テレビ")
                    		// テレビの場合product1に"テレビの残り台数は" 数 "台です。"と表示
                            ? "テレビの残り台数は" + TVnum + "台です"
                            // テレビではない場合product1に"ディスプレイの残り台数は" 数 "台です。"と表示
                            : "ディスプレイの残り台数は" + DPnum + "台です";
                	// コンソール画面にproduct1を表示
                    System.out.println(product1);
                    // 処理後、Switch文を抜ける。
                    break;

                // その他の商品は「指定外」として表示
                default:
                	// コンソール画面に"『 その他商品 』は指定の商品ではありません"と表示
                    System.out.println("『 その他商品 』は指定の商品ではありません");
                    // 処理後、Switch文を抜ける。
                    break;
            }
        }

        // Scannerを閉じる
        scanner.close();
	}
}
